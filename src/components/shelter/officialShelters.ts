function normalizeCity(value: string) {
  return value.replace(/台/g, "臺");
}

export function buildOfficialShelters(): Shelter[] {
  const seen = new Set<string>();
  const result: Shelter[] = [];
  const tagPool = [
    "貓飼料",
    "幼犬乾糧",
    "寵物尿布墊",
    "舊毛巾",
    "漂白水",
    "營業用洗潔精",
    "特大垃圾袋",
    "看護墊",
    "貓砂",
  ];

  OFFICIAL_SHELTER_DATA.forEach((record, index) => {
    const rawName = typeof record.shelter_name === "string" ? record.shelter_name.trim() : "";
    const rawAddress =
      typeof record.shelter_address === "string" ? record.shelter_address.trim() : "";
    const rawPhone = typeof record.shelter_tel === "string" ? record.shelter_tel.trim() : "";

    if (!rawName || !rawAddress) {
      return;
    }

    if (seen.has(rawName)) {
      return;
    }
    seen.add(rawName);

    const shuffled = [...tagPool].sort(() => Math.random() - 0.5);
    const urgentNeeds = shuffled.slice(0, 3 + Math.floor(Math.random() * 2));

    result.push({
      id: `official-${index}-${rawName}`,
      name: rawName,
      address: rawAddress,
      phone: rawPhone || "未提供電話",
      city: normalizeCity(
        rawAddress.match(
          /(臺北市|新北市|桃園市|臺中市|臺南市|高雄市|基隆市|新竹市|新竹縣|苗栗縣|彰化縣|南投縣|雲林縣|嘉義市|嘉義縣|屏東縣|宜蘭縣|花蓮縣|臺東縣|澎湖縣|金門縣|連江縣)/,
        )?.[1] ?? "台灣",
      ),
      needs: urgentNeeds,
      urgent_needs: urgentNeeds,
    });
  });

  return result;
}
